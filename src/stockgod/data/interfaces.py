"""Lightweight read-only provider protocols."""

from __future__ import annotations

from typing import Protocol


class PriceDataProvider(Protocol):
    def get_daily_bars(self, ticker: str, start: str | None = None, end: str | None = None) -> dict: ...


class FundamentalsProvider(Protocol):
    def get_company_profile(self, ticker: str) -> dict: ...
    def get_key_metrics(self, ticker: str) -> dict: ...
    def get_financial_ratios(self, ticker: str) -> dict: ...
    def get_income_statement(self, ticker: str) -> dict: ...
    def get_balance_sheet(self, ticker: str) -> dict: ...
    def get_cash_flow(self, ticker: str) -> dict: ...


class EarningsProvider(Protocol):
    def get_earnings_calendar(self, ticker: str) -> dict: ...


class FilingProvider(Protocol):
    def get_company_submissions(self, cik_or_ticker: str) -> dict: ...
    def get_form4_filings(self, cik_or_ticker: str) -> dict: ...


class MacroProvider(Protocol):
    def get_series(self, series_id: str) -> dict: ...


class FallbackProvider(Protocol):
    def get_quote_snapshot(self, ticker: str) -> dict: ...
